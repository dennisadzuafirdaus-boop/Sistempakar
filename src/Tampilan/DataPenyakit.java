    private void jbSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSimpanActionPerformed
      String sql = "insert into data_penyakit Values (?,?,?,?)";
    try {
        PreparedStatement stat = conn.prepareStatement(sql);
        stat.setString(1, txtIdPenyakit.getText());
        stat.setString(2, txtNmPenyakit.getText());
        stat.setString(3, jtdefinisi.getText());
        stat.setString(4, jtsolusi.getText());
    
        stat.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
        kosong();
        txtIdPenyakit.requestFocus();
    }catch (SQLException e){
        JOptionPane.showMessageDialog(null, "data gagal disimpan"+e);
    }
    datatable();
    }//GEN-LAST:event_jbSimpanActionPerformed

