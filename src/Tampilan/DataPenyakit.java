    protected void datatable() {
     Object[] Baris ={"Id Penyakit","Nama Penyakit","Definisi Penyakit","Solusi"};
    tabmode = new DefaultTableModel(null, Baris);
     String cari=txtCari.getText();
     
     
     try{
         String sql = "SELECT * FROM data_penyakit where Id_Penyakit like '%"+cari+"%' or Nama_Penyakit like'%"+cari+"%' order by Id_Penyakit asc";
         Statement stat = conn.createStatement();
         ResultSet hasil = stat.executeQuery(sql);
         while (hasil.next()){
             tabmode.addRow(new Object[]{
                hasil.getString(1),
                hasil.getString(2),
                hasil.getString(3),
                hasil.getString(4),
            });
         } 
         JtPenyakit.setModel(tabmode);
     } catch (SQLException e) {
         JOptionPane.showMessageDialog(null, "data gagal dipanggil"+e);
     }
    }

