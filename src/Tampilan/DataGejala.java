    protected void datatable() {
    // Header kolom
    Object[] Baris = {"Id Gejala", "Nama Gejala"};
    
    // Buat model baru dan set ke JTable dulu
    tabmode = new DefaultTableModel(null, Baris);
    JtGejala.setModel(tabmode);  // PENTING: set model lebih awal

    String cari = txtCari.getText();

    try {
        String sql = "SELECT * FROM data_gejala WHERE Id_Gejala LIKE '%" + cari + "%' OR Nama_Gejala LIKE '%" + cari + "%' ORDER BY Id_Gejala ASC";
        Statement stat = conn.createStatement();
        ResultSet hasil = stat.executeQuery(sql);

        while (hasil.next()) {
            String id = hasil.getString("Id_Gejala");
            String nama = hasil.getString("Nama_Gejala");
            tabmode.addRow(new Object[]{id, nama});
        }

        // Optional: refresh tampilan
        JtGejala.revalidate();
        JtGejala.repaint();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Data gagal dipanggil: " + e);
    }
}


